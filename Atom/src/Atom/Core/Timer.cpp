#include "atompch.h"
#include "Timer.h"

namespace Atom
{
    // -----------------------------------------------------------------------------------------------------------------------------
    Timer::Timer()
    {
    }

    // -----------------------------------------------------------------------------------------------------------------------------
    Timer::~Timer()
    {
    }

    // -----------------------------------------------------------------------------------------------------------------------------
    void Timer::Reset()
    {
        m_Start = std::chrono::steady_clock::now();
    }

    // -----------------------------------------------------------------------------------------------------------------------------
    void Timer::Stop()
    {
        auto endPoint = std::chrono::steady_clock::now();
        m_ElapsedTime = std::chrono::duration_cast<std::chrono::milliseconds>(endPoint - m_Start).count();
    }
}
